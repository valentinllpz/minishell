#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	flag;

	flag = 0;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			flag = 1;
			n++;
		}
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n < 10)
		ft_putchar_fd(n + '0', fd);
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10 + flag, fd);
	}
}
